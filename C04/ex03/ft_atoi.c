int	ft_atoi(char *str)
{
	int	i;
	int	sign;
	int	result;

	result = 0;
	sign = 1;
	i = 0;
	while (((str[i] >= 9 && str[i] <= 13) || str[i] == '+' || \
	str[i] == '-' || str[i] == ' ') && str[i] != '\0')
	{
		if (str[i] == '-')
			sign = -sign;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = (10 * result) + str[i] - 48;
		i++;
	}
	return (result * sign);
}
