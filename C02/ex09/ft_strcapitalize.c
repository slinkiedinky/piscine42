char	*ft_strcapitalize(char *str)
{
	int		i;
	int		up;

	i = 0;
	up = 1;
	while (str[i])
	{
		if ((str[i] >= 'a' && str[i] <= 'z')
			|| (str[i] >= 'A' && str[i] <= 'Z')
			|| (str[i] >= '0' && str[i] <= '9'))
		{
			if (up && (str[i] >= 'a' && str[i] <= 'z'))
				str[i] -= 32;
			else if (!up && (str[i] >= 'A' && str[i] <= 'Z'))
				str[i] += 32;
			up = 0;
		}
		else
			up = 1;
		i++;
	}
	return (str);
}
